#!/usr/bin/env bash
# create-project-backup-full.sh
#
# Creates a FULL project backup zip INCLUDING .git/ history (so the
# user has complete project provenance — every commit, every branch,
# every tag). Excludes only node_modules/ and .next/ (regenerable).
#
# Output: /home/z/my-project/download/aisalon-project-backup-FULL-<timestamp>.zip
#
# This is the "full version" the user requested alongside the slim
# `aisalon-project-backup-<ts>.zip` (which excludes .git).

set -euo pipefail

PROJECT_ROOT="/home/z/my-project"
DOWNLOAD_DIR="${PROJECT_ROOT}/download"
TIMESTAMP="$(date +%Y%m%d-%H%M%S)"
ZIP_NAME="aisalon-project-backup-FULL-${TIMESTAMP}.zip"
ZIP_PATH="${DOWNLOAD_DIR}/${ZIP_NAME}"

mkdir -p "${DOWNLOAD_DIR}"

STAGE="$(mktemp -d)"
trap 'rm -rf "${STAGE}"' EXIT

echo "→ Staging project at ${STAGE}/aisalon-massapro (INCLUDING .git/) ..."

mkdir -p "${STAGE}/aisalon-massapro"
cd "${PROJECT_ROOT}"

# Copy EVERYTHING except the explicitly heavy / regenerable / private dirs.
# Note: .git/ IS included — that's the whole point of the "full" version.
rsync -a \
  --exclude='node_modules' \
  --exclude='.next' \
  --exclude='.turbo' \
  --exclude='.vercel' \
  --exclude='download' \
  --exclude='tool-results' \
  --exclude='backups' \
  --exclude='.env' \
  --exclude='*.log' \
  --exclude='.gdrive-token.json' \
  --exclude='.gcp-service-account.json' \
  ./ "${STAGE}/aisalon-massapro/"

# Generate the index for the full version.
cat > "${STAGE}/aisalon-massapro/PROJECT-INDEX.md" << 'INDEX_EOF'
# AI Salon Massapro — FULL Project Backup (with .git history)

> Generated by `scripts/create-project-backup-full.sh`. This is the
> FULL version — includes the `.git/` directory so you have complete
> commit history, every branch, every tag. Excludes only
> `node_modules/`, `.next/`, `.env` (regenerable / secret).
>
> For a slim version (no .git, faster to download), see
> `aisalon-project-backup-<ts>.zip`.

