## How to restore

1. Unzip into a fresh directory.
2. `cp .env.example .env` and fill in real values (DB URL, NextAuth secret, OAuth secrets, etc.).
3. `npm install` (or `bun install`).
4. `npx prisma generate` + `npx prisma migrate deploy`.
5. `git log` to verify history is intact.
6. `npm run dev` to start the dev server.

## Chat logs

Raw chat logs live in the Z.ai gateway, not on disk. The conversation
is captured in:
- `worklog.md` (3,000+ lines, append-only)
- `core/tasks/<task-id>/brief.md` (per-task user request verbatim)
- `core/tasks/<task-id>/implementation.md` (per-task work log)

## Recent tasks (most recent first)

See `core/tasks/README.md` for the full closed-task table.
INDEX_EOF

echo "→ Zipping to ${ZIP_PATH} ..."
cd "${STAGE}"
zip -qr "${ZIP_PATH}" aisalon-massapro/

ZIP_SIZE=$(du -h "${ZIP_PATH}" | cut -f1)
ZIP_FILES=$(unzip -l "${ZIP_PATH}" | tail -1 | awk '{print $2}')
echo ""
echo "✓ FULL backup created."
echo "  Path:  ${ZIP_PATH}"
echo "  Size:  ${ZIP_SIZE}"
echo "  Files: ${ZIP_FILES}"
