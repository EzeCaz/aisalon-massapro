## How to restore

1. Unzip into a fresh directory.
2. `cp .env.example .env` and fill in real values.
3. `npm install` (or `bun install`).
4. `npx prisma generate` + `npx prisma migrate deploy`.
5. `npm run dev` to start the dev server.

## Chat logs

This backup does NOT include IM chat logs — those live in the Z.ai
gateway, not on disk. The conversation context is summarized in
`worklog.md` entries (which are written from the chat) and in
`core/tasks/<task-id>/brief.md` files (which quote the user's verbatim
request).

