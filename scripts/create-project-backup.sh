#!/usr/bin/env bash
# create-project-backup.sh
#
# Creates a downloadable zip of the entire AI Salon project, organized
# for offline browsing / Google Drive upload. Excludes heavy dirs that
# can be regenerated (node_modules, .next, .git).
#
# Output: /home/z/my-project/download/aisalon-project-backup-<timestamp>.zip
#
# What's included:
#   - src/                  (all Next.js source code)
#   - prisma/               (schema + migrations)
#   - core/                 (team, tasks, workflow, categories — all .md)
#   - public/               (brand assets, images, PDFs — but NOT uploads dir)
#   - scripts/              (utility scripts)
#   - upload/               (user uploads — kept small)
#   - mini-services/        (chat-service, quiz-service)
#   - examples/             (websocket examples)
#   - preview/              (v3.0 plan execution docs)
#   - docs/                 (CPO-PROTOCOL, PRODUCTION-ACTIVATION, etc.)
#   - *.md, *.json, *.ts, *.mjs, *.yml  (root config files)
#   - .env.example          (NOT .env — secrets excluded)
#   - worklog.md
#   - core/tasks/           (all task folders with brief/impl/closed)
#   - PROJECT-INDEX.md      (generated — table of contents)
#
# What's EXCLUDED:
#   - node_modules/         (1.3 GB — regenerable via npm install)
#   - .next/                (build artifacts)
#   - .git/                 (255 MB — git history; ask if needed)
#   - .env                  (secrets — NEVER include)
#   - bun.lock, package-lock.json  (regenerable)

set -euo pipefail

PROJECT_ROOT="/home/z/my-project"
DOWNLOAD_DIR="${PROJECT_ROOT}/download"
TIMESTAMP="$(date +%Y%m%d-%H%M%S)"
ZIP_NAME="aisalon-project-backup-${TIMESTAMP}.zip"
ZIP_PATH="${DOWNLOAD_DIR}/${ZIP_NAME}"

mkdir -p "${DOWNLOAD_DIR}"

# Stage files in a temp dir so we can add an index.md and a chat-log
# placeholder before zipping.
STAGE="$(mktemp -d)"
trap 'rm -rf "${STAGE}"' EXIT

echo "→ Staging project at ${STAGE}/aisalon-massapro ..."

# Copy the whole project, excluding heavy dirs.
mkdir -p "${STAGE}/aisalon-massapro"
cd "${PROJECT_ROOT}"

# Use rsync to exclude heavy/private dirs.
rsync -a \
  --exclude='node_modules' \
  --exclude='.next' \
  --exclude='.git' \
  --exclude='.env' \
  --exclude='.turbo' \
  --exclude='.vercel' \
  --exclude='download' \
  --exclude='tool-results' \
  --exclude='backups' \
  --exclude='*.log' \
  ./ "${STAGE}/aisalon-massapro/"

# Generate a project index so the user can navigate the zip.
cat > "${STAGE}/aisalon-massapro/PROJECT-INDEX.md" << 'INDEX_EOF'
# AI Salon Massapro — Project Backup Index

> Generated by `scripts/create-project-backup.sh`. This zip contains
> the full source code, the team operating system (`core/`), the
> worklog, and all task documentation. Excludes `node_modules/`,
> `.next/`, and `.git/` (regenerable / too heavy).

